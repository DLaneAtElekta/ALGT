    format(atom(IR), '  call void @routine_~w()\n', [RoutineName]).

%------------------------------------------------------------
% Compound Assignment
%------------------------------------------------------------

codegen_statement(assign_add(VarName, Expr), StateIn, StateOut, IR) :-
    codegen_expr(Expr, StateIn, State1, ExprResult, ExprIR),
    next_temp(State1, LoadResult, State2),
    next_temp(State2, AddResult, State3),
    ( member((VarName, _), State3.vars)
    -> VarRef = VarName, Prefix = '%'
    ;  VarRef = VarName, Prefix = '@'
    ),
    format(atom(LoadIR), '  ~w = load i64, i64* ~w~w\n',
           [LoadResult, Prefix, VarRef]),
    format(atom(AddIR), '  ~w = add i64 ~w, ~w\n',
           [AddResult, LoadResult, ExprResult]),
    format(atom(StoreIR), '  store i64 ~w, i64* ~w~w\n',
           [AddResult, Prefix, VarRef]),
    atomics_to_string([ExprIR, LoadIR, AddIR, StoreIR], IR),
    StateOut = State3.

