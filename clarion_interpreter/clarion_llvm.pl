%------------------------------------------------------------
% Variable reference
%------------------------------------------------------------

codegen_expr(var(Name), StateIn, StateOut, Result, IR) :-
    next_temp(StateIn, Result, StateOut),
    ( member((Name, _), StateIn.vars)
    -> format(atom(IR), '  ~w = load i64, i64* %~w\n', [Result, Name])
    ;  format(atom(IR), '  ~w = load i64, i64* @~w\n', [Result, Name])
    ).

%------------------------------------------------------------
% Binary operations
%------------------------------------------------------------

codegen_expr(binop(Op, Left, Right), StateIn, StateOut, Result, IR) :-
    codegen_expr(Left, StateIn, State1, LeftResult, LeftIR),
    codegen_expr(Right, State1, State2, RightResult, RightIR),
    next_temp(State2, Result, StateOut),
    binop_to_llvm(Op, LeftResult, RightResult, Result, OpIR),
    atomics_to_string([LeftIR, RightIR, OpIR], IR).

binop_to_llvm('+', L, R, Res, IR) :-
    format(atom(IR), '  ~w = add i64 ~w, ~w\n', [Res, L, R]).
binop_to_llvm('-', L, R, Res, IR) :-
    format(atom(IR), '  ~w = sub i64 ~w, ~w\n', [Res, L, R]).
binop_to_llvm('*', L, R, Res, IR) :-
    format(atom(IR), '  ~w = mul i64 ~w, ~w\n', [Res, L, R]).
binop_to_llvm('/', L, R, Res, IR) :-
    format(atom(IR), '  ~w = sdiv i64 ~w, ~w\n', [Res, L, R]).
binop_to_llvm('%', L, R, Res, IR) :-
    format(atom(IR), '  ~w = srem i64 ~w, ~w\n', [Res, L, R]).

% Comparison operators (return i64: 0 or 1)
binop_to_llvm('=', L, R, Res, IR) :-
    format(atom(IR), '  ~w.cmp = icmp eq i64 ~w, ~w\n  ~w = zext i1 ~w.cmp to i64\n',
           [Res, L, R, Res, Res]).
binop_to_llvm('==', L, R, Res, IR) :-
    binop_to_llvm('=', L, R, Res, IR).
binop_to_llvm('!=', L, R, Res, IR) :-
    format(atom(IR), '  ~w.cmp = icmp ne i64 ~w, ~w\n  ~w = zext i1 ~w.cmp to i64\n',
           [Res, L, R, Res, Res]).
binop_to_llvm('<>', L, R, Res, IR) :-
    binop_to_llvm('!=', L, R, Res, IR).
binop_to_llvm('<', L, R, Res, IR) :-
    format(atom(IR), '  ~w.cmp = icmp slt i64 ~w, ~w\n  ~w = zext i1 ~w.cmp to i64\n',
           [Res, L, R, Res, Res]).
binop_to_llvm('>', L, R, Res, IR) :-
    format(atom(IR), '  ~w.cmp = icmp sgt i64 ~w, ~w\n  ~w = zext i1 ~w.cmp to i64\n',
           [Res, L, R, Res, Res]).
binop_to_llvm('<=', L, R, Res, IR) :-
    format(atom(IR), '  ~w.cmp = icmp sle i64 ~w, ~w\n  ~w = zext i1 ~w.cmp to i64\n',
           [Res, L, R, Res, Res]).
binop_to_llvm('>=', L, R, Res, IR) :-
    format(atom(IR), '  ~w.cmp = icmp sge i64 ~w, ~w\n  ~w = zext i1 ~w.cmp to i64\n',
           [Res, L, R, Res, Res]).

