%============================================================
% Function/Procedure Calls
%============================================================

codegen_call('MESSAGE', Args, StateIn, StateOut, '0', IR) :-
    % MESSAGE builtin -> printf
    ( Args = [Arg|_]
    -> codegen_expr(Arg, StateIn, State1, ArgResult, ArgIR),
       next_temp(State1, _, StateOut),
       format(atom(CallIR), '  call i32 @puts(i8* ~w)\n', [ArgResult]),
       atom_concat(ArgIR, CallIR, IR)
    ;  IR = "", StateOut = StateIn
    ).

codegen_call(Name, Args, StateIn, StateOut, Result, IR) :-
    Name \= 'MESSAGE',
    % Check if it's a user-defined procedure
    ( member(procedure(Name, _, _, _), StateIn.procedures)
    -> codegen_user_call(Name, Args, StateIn, StateOut, Result, IR)
    ;  % Unknown function - emit warning
       next_temp(StateIn, Result, StateOut),
       format(atom(IR), '  ~w = add i64 0, 0  ; Unknown function: ~w\n', [Result, Name])
    ).

codegen_user_call(Name, Args, StateIn, StateOut, Result, IR) :-
    codegen_args(Args, StateIn, State1, ArgResults, ArgsIR),
    next_temp(State1, Result, StateOut),
    format_arg_list(ArgResults, ArgList),
    format(atom(CallIR), '  ~w = call i64 @~w(~w)\n', [Result, Name, ArgList]),
    atom_concat(ArgsIR, CallIR, IR).

codegen_args([], State, State, [], "").
codegen_args([Arg|Args], StateIn, StateOut, [Result|Results], IR) :-
    codegen_expr(Arg, StateIn, State1, Result, ArgIR),
    codegen_args(Args, State1, StateOut, Results, RestIR),
    atom_concat(ArgIR, RestIR, IR).

format_arg_list([], "").
format_arg_list([Arg], ArgStr) :-
    format(atom(ArgStr), 'i64 ~w', [Arg]).
format_arg_list([Arg|Args], ArgList) :-
    Args \= [],
    format(atom(ArgStr), 'i64 ~w, ', [Arg]),
    format_arg_list(Args, RestArgs),
    atom_concat(ArgStr, RestArgs, ArgList).

