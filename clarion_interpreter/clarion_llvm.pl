codegen_expr(string(S), StateIn, StateOut, Result, IR) :-
    add_string_literal(S, StateIn, Index, StateOut),
    atom_length(S, Len),
    Len1 is Len + 1,
    next_temp(StateOut, Result, _),
    format(atom(IR), '  ~w = getelementptr [~d x i8], [~d x i8]* @.str.~d, i64 0, i64 0\n',
           [Result, Len1, Len1, Index]).

